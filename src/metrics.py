"""Evaluation metrics for LLM benchmark outputs."""

from __future__ import annotations

import json
from typing import Any


def parse_llm_output(output: str) -> dict[str, Any]:
    """Parse an LLM JSON response into a dictionary.

    Args:
        output: Raw text generated by the LLM.

    Returns:
        Parsed JSON object when the output is valid dictionary JSON. Returns
        an empty dictionary when parsing fails or the JSON payload is not a
        dictionary.
    """
    try:
        parsed_output = json.loads(output)
    except json.JSONDecodeError:
        return {}

    if not isinstance(parsed_output, dict):
        return {}

    return parsed_output


def calculate_accuracy(
    parsed_output: dict[str, Any] | str,
    ground_truth: dict[str, Any],
) -> float:
    """Calculate strict key-value accuracy for a parsed LLM response.

    Each key from ``ground_truth`` is counted as correct only when the same key
    exists in ``parsed_output`` and the value matches exactly, including type
    and nested structures. Extra hallucinated keys in ``parsed_output`` are
    penalized by using ``max(len(ground_truth), len(parsed_output))`` as the
    denominator.

    If ``ground_truth`` is empty, an empty prediction receives ``1.0`` and any
    non-empty prediction receives ``0.0``.

    Args:
        parsed_output: Parsed LLM output dictionary or raw JSON string.
        ground_truth: Expected key-value pairs for the benchmark item.

    Returns:
        Score from ``0.0`` to ``1.0`` based on strict key-value matches while
        penalizing missing, incorrect, and hallucinated fields.
    """
    if isinstance(parsed_output, str):
        parsed_output = parse_llm_output(parsed_output)
    elif not isinstance(parsed_output, dict):
        parsed_output = {}

    if not ground_truth:
        return 1.0 if not parsed_output else 0.0

    missing = object()
    exact_matches = sum(
        1
        for key, expected_value in ground_truth.items()
        if parsed_output.get(key, missing) == expected_value
    )
    total_scored_items = max(len(ground_truth), len(parsed_output))

    return exact_matches / total_scored_items
