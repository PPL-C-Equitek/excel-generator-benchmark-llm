"""Evaluation metrics for LLM benchmark outputs."""

from __future__ import annotations

import json
from typing import Any


def calculate_accuracy(llm_output: str, ground_truth: dict[str, Any]) -> float:
    """Calculate exact-match accuracy for an LLM JSON response.

    Args:
        llm_output: JSON string generated by the LLM.
        ground_truth: Expected key-value pairs for the benchmark item.

    Returns:
        A score from 0.0 to 1.0 based on exact key/value matches.
        Malformed JSON output returns 0.0.
    """
    try:
        parsed_output = json.loads(llm_output)
    except json.JSONDecodeError:
        return 0.0

    if not isinstance(parsed_output, dict):
        return 0.0

    total_items = len(ground_truth)
    if total_items == 0:
        return 0.0

    exact_matches = sum(
        1
        for key, expected_value in ground_truth.items()
        if key in parsed_output and parsed_output[key] == expected_value
    )

    return exact_matches / total_items
