"""Evaluation metrics for LLM benchmark outputs.

This module contains scoring helpers used to compare generated LLM output
against expected benchmark answers.
"""

from __future__ import annotations

import json
from typing import Any


def calculate_accuracy(llm_output: str, ground_truth: dict[str, Any]) -> float:
    """Calculate strict key-value accuracy for an LLM JSON response.

    The generated output is parsed as JSON and must decode into a dictionary.
    Each key from ``ground_truth`` contributes equally to the final score. A
    key is counted as correct only when it exists in the parsed LLM output and
    its value matches exactly. Exact matches return ``1.0``; partial matches
    return the ratio of correct fields to total expected fields.

    Malformed JSON is treated as an invalid prediction. If ``json.loads``
    raises ``json.JSONDecodeError``, this function returns ``0.0`` instead of
    propagating the exception.

    Args:
        llm_output: JSON string generated by the LLM.
        ground_truth: Expected key-value pairs for the benchmark item.

    Returns:
        Score from ``0.0`` to ``1.0`` based on strict key-value matches.
    """
    try:
        parsed_output = json.loads(llm_output)
    except json.JSONDecodeError:
        return 0.0

    if not isinstance(parsed_output, dict):
        return 0.0

    total_expected_items = len(ground_truth)
    if total_expected_items == 0:
        return 0.0

    exact_matches = sum(
        1
        for key, expected_value in ground_truth.items()
        if key in parsed_output and parsed_output[key] == expected_value
    )

    return exact_matches / total_expected_items
